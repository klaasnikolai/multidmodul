
public class Teacher {

	private String name;
	private int nr;
	
	public Teacher(String name, int nr) {
		super();
		this.name = name;
		this.nr = nr;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNr() {
		return nr;
	}
	public void setNr(int nr) {
		this.nr = nr;
	}
	
	/**
	 * @param args
	 */
	public static void main(String[] args) {
		Teacher t = new Teacher("Ata", 1);
		System.out.println(t.getName() + " " + t.getNr());

	}

}
